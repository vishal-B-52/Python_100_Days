# Comments, Escape Sequences and Print statement

print("Hello, my name is Vishal.", 31, 21, sep=")(", end="LOL")

# sep stands for separator. It separates every argument in the Print statment with separator that you provide. 
# Different than split function which splits and forms strings on basis of where thhe separator is found.
# Default separator is whitespace.

#end is another argument which lets user decide how to end the statement. Here, once the print statment ends it prints 'LOL' just after that with no spaces. 
# Pretty useful in printing Loop results on each new line.
# # Default end is new line escape sequence.

# Both of these are optional and should be used only when required.

